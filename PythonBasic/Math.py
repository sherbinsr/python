import  math


pi = -3.14

x = 10
y = 20

# prints nearest whole integer
print(round(pi))

# prints the round up whole integer
print(math.ceil(pi))

# prints the round down to whole integer
print(math.floor(pi))

# prints the absolute value
print(abs(pi))

# powers the value
print(pow(pi,2))

# prints the squareRoot of the number
print(math.sqrt(x))

# prints maximum value
print(max(x,y))

# prints minimum value
print(min(x,y))

