
name = "helen"

# prints length of string
print(len(name))

# prints the index value of given string
print(name.find("e"))

# capitalize the first letter
print(name.capitalize())

# used for  upper case  letters
print(name.upper())

# used for  lower case  letters
print(name.lower())

# used for  checking digits
print(name.isdigit())

# used for checking does the string contains any alphabatic letters
print(name.isalpha())

# used for counting letter occurences
print(name.count("e"))

# looping string
print(name*3)

# replace string
print(name.replace("e","i"))
